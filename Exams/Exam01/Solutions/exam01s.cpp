#include <iostream>
#include <string>
using namespace std;

int main()
{
	double x, f, g, fg, gf;

	cout << "Enter a real number: ";
	cin >> x;

	f = 2 * x * x - 3 * x + 4;
	g = -4 * x * x + 8 * x - 1;
	fg = 2 * g * g - 3 * g + 4;
	gf = -4 * f * f + 8 * f - 1;

	cout << "g(f(" << x << ")) = " << gf << "\n";
	cout << "f(g(" << x << ")) = " << fg << "\n";
	return 0;
}

